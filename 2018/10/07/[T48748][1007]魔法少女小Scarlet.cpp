#include<bits/stdc++.h>
using namespace std;

int src[501][501];
int temp[501][501];
inline void cir0(int x,int y,int r) {
	for(int i=x-r;i<=x+r;i++) {
		for(int j=y-r;j<=y+r;j++) {
			temp[i][j]=src[i][j];
		}
	}
	for(int i=-r;i<=r;i++) {
		for(int j=-r;j<=r;j++) {
			src[x+j][y-i]=temp[x+i][y+j];
		}
	}
}
inline void cir1(int x,int y,int r) {
	for(int i=x-r;i<=x+r;i++) {
		for(int j=y-r;j<=y+r;j++) {
			temp[i][j]=src[i][j];
		}
	}
	for(int i=-r;i<=r;i++) {
		for(int j=-r;j<=r;j++) {
			src[x+i][y+j]=temp[x+j][y-i];
		}
	}
}
void cir(int x,int y,int r,int mode) {
	if(mode) cir1(x,y,r);
	else cir0(x,y,r);
}

inline void write(int x) {
	if(x<0) putchar('-'),x=-x;
    if(x>9) write(x/10);
    putchar(x%10+'0');
}

int id;

int n,m;

int main() {
//	freopen("input.txt","r",stdin);
//	freopen("test.txt","w",stdout);
	scanf("%d%d",&n,&m);
	for(int i=1;i<=n;i++) {
		for(int j=1;j<=n;j++) {
			src[i][j]=++id;
		}
	}
	for(int i=1;i<=m;i++) {
		int x,y,r,z;
		scanf("%d%d%d%d",&x,&y,&r,&z);
		cir(x,y,r,z);
	}
	for(int i=1;i<=n;i++) {
		for(int j=1;j<=n;j++) {
			write(src[i][j]),putchar(' ');
		}
		putchar('\n');
	}
	
	
	return 0;
}

